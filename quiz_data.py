def clean_data(quiz_data):
    cleaned_data = []

    for question in quiz_data:
        options_str = question["options"]
        # Remove leading and trailing spaces and the surrounding square brackets
        options_str = options_str.strip("[]")
        # Split the string into a list of options
        options_list = [option.strip() for option in options_str.split(",")]
        
        cleaned_question = {
            "question": question["question"],
            "options": options_list,
            "answer": question["answer"]
        }
        cleaned_data.append(cleaned_question)

    return cleaned_data

